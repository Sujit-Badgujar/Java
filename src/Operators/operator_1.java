package Operators;

public class operator_1 {

	public static void main(String[] args) {
		System.out.println(2 & 3);
		System.out.println(2 | 3);
		System.out.println(2 ^ 3);
		System.out.println(~2);
		System.out.println(12 << 1); //left shift will generally multiply by 2
		System.out.println(12 >> 1); //right shift will generally divide by 2
		System.out.println(6 << 1); //left shift will generally multiply by 2

	}

}
